// pseudocode
// Pre-req: Identify the value that must be returned.
// Pre-req: Identify the key that is associated with that value.
// Pre-req: Identify the object the key and the value are in.
// Access the value by utilizing the key and object in such a way that everytime the user inputs an object and key it will return
// that respective value.
// Return the value.

function getValue(object, key) {
  return object[key];
}
